package com.example.agilesavev2.models.users;

public class User {
    private String username, password,email,firstname,surname,id,balance, main_currency;

    public String getBalance() {
        return balance;
    }

    public String getUserName() {
        return username;
    }

    public String getFirstname() {
        return firstname;
    }

    public String getSurname() {
        return surname;
    }

    public String getId() {
        return id;
    }

    public String getPassword() {
        return password;
    }

    public String getEmail() {
        return email;
    }

    public String getUsername() {
        return username;
    }

    public String getMain_currency() {
        return main_currency;
    }
}
