package com.example.agilesavev2.models.ai;

public class Point {

    private float x,y;

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    @Override
    public String toString() {
        return "Data{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
